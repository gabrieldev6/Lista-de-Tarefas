    public void initComponentsModel(){
        //ele vai carregar dados como a lista de projeto
        //e a lista de tarefas em seus respectivos components
        projectModel = new DefaultListModel();
        loadProjects();
        
        taskModel = new TaskTableModel();
        jTable1.setModel(taskModel);
        loadTask(1);
    }
    public void loadTask(int idProject) {
        List<Task> tasks = taskController.getAll(idProject);
        taskModel.setTasks(tasks);
    }
    
    public void loadProjects() {
        //vai carregar a lista de projetos para que possa
        //ser renderizada na parte da tabela
        List<Project> projects = projectController.getAll();
        projectModel.clear();
        
        for(int i=0; i< projects.size(); i++){
            
            Project project = projects.get(i);
            projectModel.addElement(project);
        }
        jList1.setModel(projectModel);
    }
